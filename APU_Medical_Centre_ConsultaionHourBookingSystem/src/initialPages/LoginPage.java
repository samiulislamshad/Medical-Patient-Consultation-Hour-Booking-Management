    public String requestedUser(){
        String user = "";
        BufferedReader br = null;
        try {
            File rf = new File("PresentUsr.txt");
            if(!rf.exists()){
                rf.createNewFile();
                System.out.println("'PresentUsr.txt' file created.");
            }
            br = new BufferedReader(new FileReader(rf));
            user = br.lines().toArray()[0].toString();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Cant find requested user");
        }
        finally{
            try {
                br.close();
            } catch (IOException ex) {
                Logger.getLogger(SignupPage.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return user;
    }
    
