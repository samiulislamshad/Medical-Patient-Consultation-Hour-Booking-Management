    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:
        String usrname = this.usernameTf.getText();
        doctor= this.usernameTf.getText();
        String password = this.passwordTf.getText();
        String rusr = requestedUser();
        boolean isauth = false;
        
        BufferedReader br = null;
        try {
            File rf = new File("AuthData.txt");
            if(!rf.exists()){
                rf.createNewFile();
                System.out.println("'AuthData.txt' file created.");
            }
            br = new BufferedReader(new FileReader(rf));
            Object[] lines = br.lines().toArray();
            for(int i=0; i < lines.length; i++){
                String line = lines[i].toString().trim();
                String[] row = line.split(",");
                
                if(usrname.equals(row[1]) && password.equals(row[4])){
                    
                    File file= new File("LoginReference.txt");
                    if(!file.exists()){
                        file.createNewFile();
                        System.out.println("'LoginReference.txt' file created.");
                    }else{
                        boolean x=file.delete();
                        if(x==true){
                            file.createNewFile();
                        }
                    }
                    
                    FileWriter fw = new FileWriter(file, true);
                    fw.write(usrname+","+password+"\n");
                    fw.close();
                    mobile=row[3];
                    isauth = true;
                    this.user = row[5];
                }
            }
            if (isauth){
                if ("admin".equals(this.user) && rusr.equals(this.user)) {
                    //saving username reference
                    
                    AdminPage ap = new AdminPage();
                    ap.setVisible(true);
                    this.dispose();
                }
                else if ("doctor".equals(this.user) && rusr.equals(this.user)){
                    //JOptionPane.showMessageDialog(this, "Logged in as Doctor! ");
                    
                    DoctorPage dp = new DoctorPage();
                    dp.setVisible(true);
                    this.dispose();
                }
                else{
                    System.out.println(this.user+" -- "+ rusr);
                    JOptionPane.showMessageDialog(this, "You are not " + rusr.toUpperCase());
                }
            }
            else
                JOptionPane.showMessageDialog(this, "Authentication Discarded. Invalid Input");
            
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Authentication Discarded. Technical Error");
        }finally{
            try {
                br.close();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Authentication Discarded. Technical Error");;
            }
        }
    }//GEN-LAST:event_loginBtnActionPerformed

