    private boolean checkEmail(String email){
        String emailRegex = "^[\\w!#$%&'*+/=?`{|}~^-]+(?:\\.[\\w!#$%&'*+/=?`{|}~^-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
        
        Pattern pat = Pattern.compile(emailRegex); 
        if (email == null | email == "") 
            return false; 
        else
            return pat.matcher(email).matches(); 
    }
    
    private boolean checkContactno(String cno){
        Pattern p = Pattern.compile("(\\+(\\d+)0|0)?[1-9][0-9]{9}"); 
	Matcher m = p.matcher(cno);
        
        if ("".equals(cno)) 
            return false; 
        else
            return (m.find() && m.group().equals(cno));
    }
    
    private void writeFile(int index, String usrname, String email, String contact, String password, String authuser){
        FileWriter fw = null;
        try {
            File rf = new File("AuthData.txt");
            if(!rf.exists()){
                rf.createNewFile();
                System.out.println("'AuthData.txt' file created.");
            }
            fw = new FileWriter(rf, true);
            fw.append(index+","+usrname+","+email+","+contact+","+password+","+authuser+"\n");
            JOptionPane.showMessageDialog(this, "New user ("+authuser+") data has been added successfull!");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Problem occured in accessing authentication file.");
        }finally{
            try {
                fw.flush();
                fw.close();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Problem occured in accessing authentication file.");
            }
        }
    }
    
    private int getNewIndex(){
        BufferedReader br = null;
        int lineno = 0;
        try {
            File rf = new File("AuthData.txt");
            if(!rf.exists()){
                rf.createNewFile();
                System.out.println("'AuthData.txt' file created.");
            }
            br = new BufferedReader(new FileReader(rf));
            lineno = br.lines().toArray().length+1;
            br.close();
            return lineno;
        } catch (IOException ex) {
            try {
                br.close();
                return -1;
            } catch (IOException ex1) {
                JOptionPane.showMessageDialog(this, "Authentication Discarded. Technical Error");;
            }
            JOptionPane.showMessageDialog(this, "Authentication Discarded. Technical Error");
        }
        return 1;
    }
    

